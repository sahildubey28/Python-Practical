# Q7. Write a Python program to parse a string to Float & Integer

balance_str = "1500"
balance_int = int(balance_str)

# print the type
print(type(balance_int))
# print the value
print(balance_int)

balance_str = "1500.4"
balance_float = float(balance_str)

# print the type
print(type(balance_float))
# print the value
print(balance_float)

# output : 

# <class 'int'>
# 1500
# <class 'float'>
# 1500.4
