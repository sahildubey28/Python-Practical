# Q40. Write a Python program that takes input from the user and displays that input back in upper and lower cases.

user_input = input("Enter a String : ")
print("String in UpperCase : ", user_input.upper())
print("String in LowerCase : ", user_input.lower())

# Output:

# Enter a String : English
# String in UpperCase :  ENGLISH
# String in LowerCase :  english
